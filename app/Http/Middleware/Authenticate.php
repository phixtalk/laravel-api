    }

    /**
     * Handle an incoming request.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \Closure  $next
     * @param  string[]  ...$guards
     * @return mixed
     *
     * @throws \Illuminate\Auth\AuthenticationException
     */

    /**
     * This function was copied over from Illuminate\Auth\Middleware\Authenticate
     * In order to override its functionality
     * So now we get the token from the cookie instead of from the bearer token
     * and manually set the headers with it
     */
    public function handle($request, Closure $next, ...$guards)
    {
        if ($jwt = $request > cookie('jwt')) {
            $request->headers->set('Authorization', 'Bearer ' . $jwt);
        }

        $this->authenticate($request, $guards);

        return $next($request);
    }
}
